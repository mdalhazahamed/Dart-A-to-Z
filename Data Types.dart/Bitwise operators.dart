void main() {
  int a = 30; //integer 
  int b = 40; //integer

   print(a & b); //bitwise AND operators
   print(a | b); // bitwise OR oprators
   print(a ^ b); //bitwise XOR opertor
   print(a ~/ b); //NOT operators
   print(a << b); //lift shift operators
   print(a >> b); //right shift operators
}